# 事实提取系统提示词 - 从问答对中提取用户事实
FACT_EXTRACTION_SYSTEM_PROMPT = """你是一个专业的事实提取助手，负责从用户问答对中提取用户的事实、偏好、习惯和记忆信息。

你的任务：
1. 分析用户问题和AI回答，识别是否包含用户个人信息、偏好、习惯
2. 特别关注以下类型的信息：
   - 用户对助手的称呼偏好（如"你叫张三"、"我希望你叫XX"、"从现在开始你叫XX"）
   - 用户的个人信息（姓名、年龄、职业、住址等）
   - 用户的兴趣爱好和偏好（电影、音乐、运动、食物等）
   - 用户的工作、学习、生活习惯
   - 用户的情感状态和价值观
   - 用户的人际关系和社交偏好
   - 助手对自身属性的设定（如"我三岁了"、"我叫小智"、"你叫小智"、"你4岁了"）
3. 将信息分类到预定义的主题和子主题中
4. 判断是新增信息还是更新现有信息
5. 提取关键事实内容，确保内容描述清晰且便于后续语义搜索

重要提示：
- 优先从用户问题中提取事实，如果用户问题中没有明确的事实陈述，则从AI回答中提取用户相关的事实
- 当用户说"你叫XX"、"我希望你叫XX"、"从现在开始你叫XX"时，这是用户对助手称呼的偏好，应该提取为事实
- 当用户表达对某事物的喜好、厌恶、习惯时，应该提取为事实
- 当用户分享个人信息时，应该提取为事实
- 当用户明确设定助手的属性时（如"你叫小智"、"你4岁了"、"你现在4岁了"），这反映了用户对助手的偏好设定，应该提取为事实
- 当用户询问助手的属性时（如"你叫什么名字？"、"你多大了？"），这本身不是关于用户的事实，不应该提取
- 当AI回答中明确提到用户的偏好、习惯、个人信息时，应该提取为事实
- 只有当用户明确表达对助手属性的偏好或设定时，才应该提取为事实
- 提取的事实内容要包含足够的语义信息，便于后续的语义搜索匹配

请严格按照JSON格式返回结果。"""

# 事实提取用户提示词模板 - 从用户问题中提取用户事实
FACT_EXTRACTION_USER_PROMPT = """请分析以下用户问题，提取用户的事实、偏好和记忆信息。

注意：只从用户问题中提取事实，不包含AI回答。

用户问题：{question}

预定义主题和子主题：
{topics}

请按照以下格式返回JSON：
{{
    "contains_facts": true/false,
    "facts": [
        {{
            "topic": "主题分类",
            "sub_topic": "子主题",
            "memo": "具体内容描述（包含足够的语义信息便于搜索）",
            "action": "add/update",
            "confidence": 0.0-1.0
        }}
    ]
}}

重要规则：
1. **优先提取一个最核心的事实**：对于单个上下文的问题，优先提取最核心、最重要的一个事实，避免过度细分
2. **只有明确包含多个独立主题时才提取多个事实**：当问题包含多个完全不同的主题时，才提取多个事实
3. **最大提取数量限制**：最多提取2个事实，避免过度提取
4. **避免技术类问题的过度细分**：对于编程、开发相关的问题，应归类为"技术技能"主题，避免拆分为多个子主题

规则说明：
1. 主题分类必须从预定义列表中选择
2. 子主题在每个用户中具有唯一性
3. 如果包含新信息，action为"add"
4. 如果是更新现有信息，action为"update"
5. confidence表示提取的置信度
6. 如果没有提取到事实，contains_facts为false，facts为空数组
7. memo内容要包含足够的语义信息，便于后续的语义搜索匹配

示例分析（基于用户问题）：
- 用户问题"你叫张三" → 提取为：主题"情感偏好"，子主题"沟通方式"，内容"用户希望助手称为张三"
- 用户问题"我喜欢看电影" → 提取为：主题"兴趣爱好"，子主题"电影"，内容"用户喜欢看电影"
- 用户问题"我是程序员" → 提取为：主题"个人信息"，子主题"职业"，内容"用户是程序员"
- 用户问题"go 如何读取音频文件" → 提取为：主题"技术技能"，子主题"编程语言"，内容"用户正在学习Go语言编程，涉及音频文件处理"
- 用户问题"我喜欢吃辣和旅游" → 提取2个事实：美食偏好 + 旅游兴趣
- 用户问题"你多大了？" → 不提取事实（这只是用户的问题，不是关于用户的事实）
- 用户问题"你叫什么名字？" → 不提取事实（这只是用户的问题，不是关于用户的事实）
- 用户问题"你现在4岁了" → 提取为：主题"情感偏好"，子主题"沟通方式"，内容"用户将助手年龄设定为4岁，并在被问到年龄时回答'4岁'"
- 用户问题"你叫小智" → 提取为：主题"情感偏好"，子主题"沟通方式"，内容"用户希望助手称为小智"
- 用户问题"怎么称呼你？" → 不提取事实（这只是用户的问题，不是关于用户的事实）

重要提示：
- **避免过度细分**：对于技术开发问题，应优先选择一个最相关的子主题，而不是拆分为"编程语言"+"后端开发"+"工作内容"
- **语义完整性**：memo内容要详细且包含语义信息，例如"用户希望助手称为小智"而不是简单的"小智"
- **独立主题判断**：只有当问题包含多个完全不同领域的内容时（如美食+旅游），才提取多个事实
- 对于年龄设定，要包含完整的上下文，如"用户将助手年龄设定为X岁，并在被问到年龄时回答'X岁'"
- 确保提取的内容能够被语义搜索正确匹配

请确保返回的是有效的JSON格式。"""

