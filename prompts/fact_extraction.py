# 记忆更新决策提示词
MEMORY_UPDATE_DECISION_PROMPT = """请判断是否需要更新用户的记忆信息。

当前对话：{current_conversation}
用户历史事实：{user_facts}

请仔细分析并判断：

1. **冲突情况**：新事实与现有事实是否冲突？
   - 冲突示例：原来"喜欢吃辣" → 现在"喜欢吃清淡"（口味偏好改变）
   - 冲突示例：原来"不喜欢运动" → 现在"喜欢运动"（态度完全相反）

2. **补充情况**：新事实是否为现有事实的补充？
   - 补充示例：原来"喜欢跑步" → 现在"喜欢游泳"（都是运动爱好，应该合并）
   - 补充示例：原来"会英语" → 现在"会法语"（都是语言技能，应该合并）

3. **无关情况**：新事实与现有事实无关？
   - 无关示例：原来"喜欢音乐" → 现在"住在北京"（完全不同的主题）

**决策规则：**
- **update**：仅当新事实与现有事实存在冲突时（态度、偏好完全相反）
- **merge**：当新事实与现有事实属于同一类别且不冲突时（如多种运动爱好、多种技能等）
- **add**：当没有找到相同主题和子主题的现有事实时

返回JSON格式：
{{
    "needs_update": true/false,
    "update_type": "add/update/merge",
    "reason": "详细说明判断理由"
}}"""

